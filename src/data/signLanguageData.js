// Helper functions for data access
export const getSignsByCategory = (category) => {
    return signLanguageData[category]?.signs || []
}

export const getRandomSignsFromCategory = (category, count = 5) => {
    const signs = getSignsByCategory(category)
    return signs.sort(() => 0.5 - Math.random()).slice(0, count)
}

export const getAllCategories = () => {
    return Object.keys(signLanguageData)
}

export const getSignById = (id) => {
    for (const category of Object.values(signLanguageData)) {
        const sign = category.signs.find(sign => sign.id === id)
        if (sign) return sign
    }
    return null
}

// Mock API service for future extensibility
export class SignLanguageAPI {
    static async fetchSignData(category, limit = 10) {
        // Simulate API delay
        await new Promise(resolve => setTimeout(resolve, 500))

        const categoryData = signLanguageData[category]
        if (!categoryData) {
            throw new Error(`Category '${category}' not found`)
        }

        return {
            success: true,
            data: {
                ...categoryData,
                signs: categoryData.signs.slice(0, limit)
            }
        }
    }

    static async searchSigns(query) {
        // Simulate API delay
        await new Promise(resolve => setTimeout(resolve, 300))

        const allSigns = []
        Object.values(signLanguageData).forEach(category => {
            allSigns.push(...category.signs)
        })

        const filteredSigns = allSigns.filter(sign =>
            sign.word.toLowerCase().includes(query.toLowerCase()) ||
            sign.description.toLowerCase().includes(query.toLowerCase()) ||
            sign.category.toLowerCase().includes(query.toLowerCase())
        )

        return {
            success: true,
            data: filteredSigns
        }
    }
} 